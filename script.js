const langButtons = document.querySelectorAll("[data-lang]");
let currentLang = "ru";
const projectCarousels = [];
const drawingViewers = [];

function setLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[lang][key]) element.textContent = translations[lang][key];
  });
  document.title = translations[lang].metaTitle;
  langButtons.forEach((button) => button.classList.toggle("active", button.dataset.lang === lang));
  projectCarousels.forEach((carousel) => updateDynamicProjectTitle(carousel));
  drawingViewers.forEach((viewer) => updateDrawing(viewer));
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

document.querySelectorAll(".project-carousel").forEach((carousel) => {
  const track = carousel.querySelector("[data-carousel-track]");
  const status = carousel.querySelector("[data-carousel-status]");
  const slides = carousel.querySelectorAll(".project-slide");
  const title = carousel.querySelector("[data-dynamic-project-title]");
  const state = { track, status, title, slides, total: slides.length, current: 0 };
  projectCarousels.push(state);

  function updateCarouselStatus() {
    status.textContent = `${state.current + 1} / ${state.total}`;
    updateDynamicProjectTitle(state);
    syncCarouselVideos(state);
  }

  function scrollToSlide(index) {
    state.current = Math.max(0, Math.min(state.total - 1, index));
    track.scrollTo({ left: state.current * track.clientWidth, behavior: "smooth" });
    updateCarouselStatus();
  }

  carousel.querySelector("[data-carousel-prev]").addEventListener("click", () => scrollToSlide(state.current - 1));
  carousel.querySelector("[data-carousel-next]").addEventListener("click", () => scrollToSlide(state.current + 1));

  track.addEventListener("scroll", () => {
    const nextSlide = Math.round(track.scrollLeft / track.clientWidth);
    if (nextSlide !== state.current) {
      state.current = nextSlide;
      updateCarouselStatus();
    }
  });

  updateCarouselStatus();
});

function updateDynamicProjectTitle(state) {
  if (!state.title) return;
  const slide = state.slides[state.current];
  state.title.textContent = currentLang === "en" ? slide.dataset.titleEn : slide.dataset.titleRu;
}

function configureVideo(video) {
  video.playbackRate = video.dataset.normalVideo !== undefined ? 1 : video.dataset.slowVideo !== undefined ? 0.45 : 0.7;
}

function syncCarouselVideos(state) {
  state.slides.forEach((slide, index) => {
    slide.querySelectorAll("video").forEach((video) => {
      configureVideo(video);
      if (index === state.current) {
        video.play().catch(() => {});
      } else {
        video.pause();
        video.currentTime = 0;
      }
    });
  });
}

document.querySelectorAll(".drawings-stage").forEach((stage) => {
  const image = stage.querySelector("[data-drawing-image]");
  const viewer = {
    image,
    status: stage.querySelector("[data-drawing-status]"),
    prev: stage.querySelector("[data-drawing-prev]"),
    next: stage.querySelector("[data-drawing-next]"),
    prefix: image.dataset.drawingPrefix,
    files: image.dataset.drawingFiles ? image.dataset.drawingFiles.split(",") : null,
    total: Number(image.dataset.drawingTotal || 1),
    current: 1
  };
  drawingViewers.push(viewer);
  viewer.prev.addEventListener("click", () => {
    viewer.current = viewer.current === 1 ? viewer.total : viewer.current - 1;
    updateDrawing(viewer);
  });
  viewer.next.addEventListener("click", () => {
    viewer.current = viewer.current === viewer.total ? 1 : viewer.current + 1;
    updateDrawing(viewer);
  });
  updateDrawing(viewer);
});

function updateDrawing(viewer) {
  const number = String(viewer.current).padStart(2, "0");
  viewer.image.src = viewer.files ? `${viewer.prefix}${viewer.files[viewer.current - 1]}` : `${viewer.prefix}${number}.png`;
  viewer.image.alt = `${translations[currentLang].drawingStatus} ${viewer.current}`;
  viewer.status.textContent = `${translations[currentLang].drawingStatus} ${viewer.current} / ${viewer.total}`;
}

document.querySelectorAll("video").forEach((video) => {
  configureVideo(video);
  if (!video.closest(".project-carousel")) video.play().catch(() => {});
});

